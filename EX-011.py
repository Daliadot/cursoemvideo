altura = float(input('Digite a Altura em metros da parede:'))
largura= float(input('digite a largura em metros da parede:'))
area= altura*largura
tinta= area/2
print(f'Sua parede tem a dimensão de {largura}X{altura} e sua área é de {area}m².')
print(f'para essa parede, você precisará de {tinta}L de tinta')